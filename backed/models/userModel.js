import mongoose, { mongo, Mongoose } from "mongoose";
import validator from "validator";

const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },

    email: {
        type: String,
        required: true,
        unique: true,
        isLowercase: true,
        trim: true
    },

    password: {
        type: String,
        required: true,
    },
}, {
    timestamps: true
})

export default mongoose.models.User || mongoose.model("user", userSchema)